void main(){
   CanvasElement mmo = querySelector("#minimapobjects");
   
   MiniMap miniMap = new MiniMap(querySelector("#minimap"), mmo, map); 
   miniMap.drawMiniMap();
   
   Player p = new Player(map);
   Raycaster r = new Raycaster(p, map);
   r.initScreen();
   
   GameLoopHtml objectLoop = new GameLoopHtml(mmo);
   objectLoop
      ..onUpdate = ((objectLoop) {
         p.move();
      })
      ..onRender = ((objectLoop) {
         miniMap.updateObjects(p);
         r.castRays();
      })
      ..start();
}


